def to_bin_array(encoded_caracter):
    bin_array = np.zeros((7, 5), dtype=int)
    for row in range(0, 7):
        current_row = encoded_caracter[row]
        for col in range(0, 5):
            bin_array[row][4-col] = current_row & 1
            current_row >>= 1
    return bin_array


def draw_char(i):
    monocromatic_cmap = plt.get_cmap('binary')
    encoded_character = to_bin_array(font[i])
    print(encoded_character)
    heatmap(
        encoded_character, 
        linewidths=0.2, 
        cbar=False, 
        square=True,
        cmap=monocromatic_cmap, 
        linecolor='k')
    plt.title("Character: "+labels[i])
    plt.show()

def draw_flattened_char(flattened, i):
    monocromatic_cmap = plt.get_cmap('binary')
    encoded_character = flattened.reshape((7, -1))
    heatmap(
        encoded_character, 
        linewidths=0.2, 
        cbar=False, 
        square=True,
        cmap=monocromatic_cmap, 
        linecolor='k')
    plt.title("Character: "+labels[i])
    plt.show()

def get_font_as_xis(): 
    xis = []
    for x in font:
        char_as_mat = to_bin_array(x)
        char_as_mat = char_as_mat.reshape((-1))
        xis.append(char_as_mat)
    return np.array(xis)


